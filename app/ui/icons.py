def app_icon(name: str, color: str = "#a0aabe") -> QIcon:
    """Load an SVG icon from the embedded icon dictionary.

    ``color`` replaces every ``currentColor`` / hard-coded ``fill``
    value so the icon is visible on the dark theme.  Pass an explicit
    hex color to tint the icon (e.g. ``"#dfc477"`` for gold).
    """
    icon = QIcon()
    svg_data = ICONS.get(name)
    if svg_data:
        tinted = svg_data.replace("currentColor", color)
        pixmap = QPixmap()
        pixmap.loadFromData(tinted.encode("utf-8"))
        icon.addPixmap(pixmap)
    return icon


def app_logo_icon() -> QIcon:
    """Return the main application logo icon from assets."""
    if LOGO_PATH.exists():
        return QIcon(str(LOGO_PATH))
    return app_icon("logo.svg")  # Fallback to embedded SVG


def app_logo_pixmap(size: int = 64) -> QPixmap:
    """Return the main application logo as a QPixmap."""
    if LOGO_PATH.exists():
        pixmap = QPixmap(str(LOGO_PATH))
        return pixmap.scaled(size, size)
    return QPixmap()
